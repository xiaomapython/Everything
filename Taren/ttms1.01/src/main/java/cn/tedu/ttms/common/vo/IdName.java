package cn.tedu.ttms.common.vo;
/**
 * 定义一个值对象，通过此对象封装id和name
 * */
public class IdName {
	private Integer id;
	private String name;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
}
